#algoritmo que muestre el nombre del mes, segun el numero ingresado del 1 al 12
mes = int(input("MES [1 - 12]: "))

if (mes == 1):
    print("ENERO")
elif (mes == 2):
    print("FEBRERO")
elif (mes == 3):
    print("MARZO")
elif (mes == 4):
    print("ABRIL")
elif (mes == 5):
    print("MAYO")
elif (mes == 6):
    print("JUNIO")
elif (mes == 7):
    print("JULIO")
elif (mes == 8):
    print("AGOSTO")
elif (mes == 9):
    print("SEPTIEMBRE")
elif (mes == 10):
    print("OCTUBRE")
elif (mes == 11):
    print("NOVIEMBRE")
elif (mes == 12):
    print("DICIEMBRE")
else:
    print("MES INVALIDO")