#algoritmo que muestre el nombre completo de un programa al ingresar su primera letra
letra = input("Letra [V - P - C - J - F]: ").upper()
if (letra == "V"):
    print("Visual Basic")
elif (letra == "P"):
    print("Pascal")
elif (letra == "C"):
    print("C#")
elif (letra == "J"):
    print("Java")
elif (letra == "F"):
    print("Fortran")